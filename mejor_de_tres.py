n1 = int(input("Introduce el numero 1: "))
n2 = int(input("Introduce el numero 2: "))
n3 = int(input("Introduce el numero 3: "))
print("El mayor es: {}".format(max(n1,n2,n3)))
print("El menor es: {}".format(min(n1,n2,n3)))

print("El mayor valor entre {}, {} y {} es: {}, y el menor valor es: {}".format(n1,n2,n3, max(n1,n2,n3),min(n1,n2,n3)))